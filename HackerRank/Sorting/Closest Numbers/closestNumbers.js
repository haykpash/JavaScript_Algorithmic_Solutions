function closestNumbers(arr) {
  let diff = 200000
  //let diff = Number.MAX_SAFE_INTEGER + 1
  let a = []
  arr.sort((a, b) => a - b)
  for (let i = 0; i < arr.length - 1; i++) {
    let sum = Math.abs(arr[i] - arr[i + 1])
    if (sum < diff) {
      a = []
      diff = sum
      a.push(arr[i], arr[i + 1])
    } else if (sum === diff) a.push(arr[i], arr[i + 1])
  }
  return a
}
